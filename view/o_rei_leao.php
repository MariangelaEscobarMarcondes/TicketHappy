				<h2>O REI LEÃO - O MUSICAL</h2>
				
				<p class="texto_justificado">
				O espetáculo Rei Leão - O Musical, sucesso de bilheteria em 2013, 
				reestreia no dia 8 de janeiro, no Teatro Renault. Encenada em 15 
				países e traduzida para oito línguas diferentes, a adaptação do filme 
				‘O Rei Leão’ fica em cartaz em São Paulo por tempo indeterminado. 
				</p class="texto_justificado">
				<p class="texto_justificado">Eleito em 2012 a maior bilheteria da história da Broadway, com 
				arrecadação de US$853,8 milhões desde sua primeira apresentação, a 
				produção já vendeu mais de 250 mil ingressos no Brasil em menos de 
				seis meses em cartaz.</p>
				<p class="texto_justificado">A produção apresenta Osvaldo Mil no papel de “Scar”, César Mello é 
				“Mufasa”,Phindile Mkhize é “Rafiki”, Claudio Galvan é “Zazu”, Marcelo Klabin 
				é “Pumba”,Ronaldo Reis é “Timão”, Tiago Barbosa é “Simba”, Josi Lopes é “Nala”, 
				Jorge Neto é “Banzai”, Juliana Peppi é “Shenzi”, e Felippe Morais é “Ed”.</p>
				<p class="texto_justificado">O papel do “Jovem Simba” é alternado entre Gustavo Bonfim, Henrique Filgueiras, 
				Yudichi Taniguti e Matheus Braga, e o papel de “Jovem Nala” é alternado entre Any 
				Gabriele, Karollyne Nascimento, Lais Dias e Ysa Paula.</p>
			
