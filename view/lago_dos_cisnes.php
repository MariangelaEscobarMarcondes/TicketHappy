				<h2>O LAGO DOS CISNES</h2>
				
				<p class="texto_justificado">
				"O Lago dos Cisnes", com o passar dos anos e com a evolução do ballet tem se tornado o mais
				popular de todos. É o ballet que captura como nenhum outro todas as emoções humanas, da 
				esperança ao desespero, do terror ao carinho, da melancolia ao êxtase. A versão que sobrevive 
				até hoje é o sucesso que dois russos juntos coreografaram. Eram eles Marius Petipa e Lev Ivanov, 
				o brilhante coreógrafo que desenvolveu toda a ideia dramática central de uma moça presa em um corpo 
				de um pássaro, sendo assim representado pela leveza dos movimentos dos braços da bailarina, 
				as articulações de seus pés e a posição de sua cabeça e o alongamento do pescoço. 
				</p>
				
				<p class="texto_justificado">"O Lago dos Cisnes" conta a história do jovem Príncipe Siegfried, 
				que se apaixona por Odette, uma rainha que foi transformada em um cisne por um feiticeiro malvado. 
				Odette o explica que ela é destinada a permanecer como esta estranha criatura, até ser resgatada 
				por um homem que jure amor eterno a ela. Encantado com a beleza dela, o Príncipe promete jurar 
				amor eterno ao mundo. Mas logo em seguida, na festa em sua homenagem por seu 21º aniversário ele
				é enganado pelo feiticeiro, von Rothbart, declarando seu amor por Odile, uma gêmea malvada de 
				Odette, o levando a jurar amor por ela. Quando ele se deu por si, correu de volta ao lago. Lá 
				ele combate Von Rothbart e destrói seu poder e assim os apaixonados podem ficar juntos e felizes 
				para sempre. Este é um dos mais difíceis balés tecnicamente, em parte pela honrosa bailarina 
				italiana chamada Pierina Legnani. </p>
				
			
